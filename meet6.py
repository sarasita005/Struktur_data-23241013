# perbandingan >, <, <=, >=, ==, !=, is, isnot

x = 3
y = 4

# lebih besar >
hasil = x > y
print (x, '<', y, '=', hasil)

# lebih kecil <
hasil = x < y 
print (x, '<', y, '=', hasil)

# lebih besar>=
hasil = x >= y
print (x, '>=', y, '=', hasil)
print (x, '>=', 3, '=', hasil)

# lebih besar <=

hasil = x <= y
print (x, '<=', y, '=', hasil)
print (x, '<=', 3, '=', hasil)

